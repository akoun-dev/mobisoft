// Fonctions utilitaires pour les donn√©es des pays
export const getSupportedCountries = (): AfricanCountry[] => {
    return Object.values(AFRICA_COUNTRIES).filter(
        country => country.is_supported
    )
}

export const getCountryByCode = (code: string): AfricanCountry | undefined => {
    return AFRICA_COUNTRIES[code.toUpperCase()]
}

export const getCountriesByRegion = (region: string): AfricanCountry[] => {
    return Object.values(AFRICA_COUNTRIES).filter(country =>
        country.subregion.toLowerCase().includes(region.toLowerCase())
    )
}

export const getCountriesByLanguage = (language: string): AfricanCountry[] => {
    return Object.values(AFRICA_COUNTRIES).filter(country =>
        country.languages.some(lang =>
            lang.toLowerCase().includes(language.toLowerCase())
        )
    )
}

export const searchCountries = (query: string): AfricanCountry[] => {
    const searchTerm = query.toLowerCase()
    return Object.values(AFRICA_COUNTRIES).filter(
        country =>
            country.name.toLowerCase().includes(searchTerm) ||
            country.name_fr.toLowerCase().includes(searchTerm) ||
            country.capital.toLowerCase().includes(searchTerm) ||
            country.capital_fr.toLowerCase().includes(searchTerm)
    )
}

